#include "get_next_line.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*ptrlst;

	ptrlst = malloc(sizeof(*ptrlst) * 1);
	if (!ptrlst)
		return (NULL);
	ptrlst->next = NULL;
	ptrlst->content = content;
	return (ptrlst);
}

int	ft_lstsize(t_list *lst)
{
	int	i;

	i = 0;
	while (lst)
	{
		i++;
		lst = lst->next;
	}
	return (i);
}